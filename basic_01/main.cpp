#include <iostream>

using namespace std;

int main()
{
    int add, sub, mul, div, mod;
    add = 4 + 5;
    sub = 4 - 5;
    mul = 4 * 5;
    div = 4 / 5;
    mod = 4 % 5;
    
    cout << "Addition: " << add << endl;
    cout << "Subtraction: " << sub << endl;
    cout << "Multiplication: " << mul << endl;
    cout << "Division: " << div << endl;
    cout << "Modulus: " << mod << endl;

    // cout << add << endl; 
    return 0;
}